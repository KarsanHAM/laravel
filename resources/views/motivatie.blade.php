            <h3> Wat is motivatie?</h3>
            <p>Volgends The Business Dictionary bestaat motivatie uit interne en externe factoren die een verlangen en
                energie stimuleren in mensen die constant geintresseerd zijn en gefocused op hun taak, rol of onderwerp of
                moeite er in stoppen om een doel te bereiken.(<a href="#2">2</a>)<br>
                Intrinsieke motivatie is een soort motivatie en volgens "The American Psychological Association"
                is het het initiatief om mee te doen met een bepaalde activiteit die komt van genot dat je uit die activiteit
                haalt, in tegenstelling dan het voor extra dingen doen die je er uit haalt.(<a href="#3">3</a>)<br>
                Als er intrinsieke motivatie is is er natuurlijke ook extrensieke motivatie en dat is ook weer volgends de
                "APA" een extern intiatief om mee te doen in specifieke activiteit, vooral de motivatie die komt van een
                potenti&#235;le straf bij falen of een beloning bij succes.(<a href="#4">4</a>)
            </p>
        </div>
        <div class="col-xl-6 col-12 allcols">
            <h3> Dan Pink's TED-talk over motivatie</h3>
            <p>
                Volgends Dan Pink in zijn TED talk: "the puzzle of motivation"(<a href="#5">5</a>). Is ons managementmodel voor business
                verkeerd opgebouwd.<br>
                Zijn talk presenteert een stelling die zegt dat extrensieke motivatie factoren(zoals bijvoorbeeld geld)
                creativiteit van werknemeners beperken en dat intrinsieke factoren creativiteit juist versterken.
                Dat in combinatie met het feit dat we in een samenleving wonen waar simpele dingen geautomatiseerd worden en
                voor ons gedaan worden door machines, creativeit een steeds belangrijkere rol gaat spelen in onze
                samenleving.<br>
                De conclusie van zijn Ted-Talk zegt dat het tijd is voor het veranderen van ons managementmodel en niet tijd
                is om nog meer van dezelfde fouten te maken binnen bedrijfsmanagement.
            </p>
        </div>
    </div>
    <div class="row mt-5">
        <div class="col-xl-6 col-12 allcols">
            <h3>Prestatiemotivatie</h3>
            <p>Pretatiemotivatie is de wil om goed te willen presteren, om doelen te behalen en succesvol te willen zijn.
                Deze motivatie hangt samen met de motivatie om falen te voorkomen.<br>
                Maar wat doet iemand met hoge prestatiemotivatie volgens carri&#235;retijger(<a href="#6">6</a>)? En hoe weegt dat op tegen wat Dan
                Pink over motivatie zegt?
            <ul>
                <li>"Is vaak intrinsiek gemotiveerd."<br>
                    Dit is volgends Dan de sterkste motivator voor velen. Hij geeft in zijn Ted-Talk ook voorbeelden van
                    experimenten waarin mensen die intrinsiek gemotiveerd worden beter presteren dan mensen die extrensiek
                    gemotiveerd worden.</li>
                <li>"Heeft een voorkeur voor werkzaamheden die een beroep doen op zijn competenties."<br>
                    klopt ook (indirect) volgens Dan Pink. Je competenties liggen vaak bij dingen die je leuk vind om te
                    doen, want je doet ze veel! Daardoor lever je betere prestaties.</li>
                <li>"Kiest graag taken die net boven zijn kunnen liggen."<br>
                    Hier maakt Dan Pink ook inderect een opmerking over. Een taak waar je meer creatief voor moet zijn en waar
                    je voor na moet denken om tot een oplossing te komen komt steeds vaker voor. Deze taken worden ook steeds
                    belangrijker, omdat simpele taken steeds meer geautomatiseerd worden. Daardoor word jezelf uitdagen om
                    dingen te doen die je nog niet kunt een goede manier om jezelf op te laten vallen op de werkvloer. Het
                    draagt ook bij aan een van de 3 kernen waar Dan Pink zijn moderne business plan mee beschrijft: "Mastery:
                    The desire to get better and better at what we do."</li>
            </p>
        </div>
        <div class="col-xl-6 col-12 allcols">
            <h3>Mijn prestatiemotivatie</h3>
            <p>Resultaat van een prestatiemotivatietest(<a href="#8">8</a>):<br>
                Je bent in redelijke mate intrinsiek gemotiveerd en in redelijke mate extrinsiek gemotiveerd. <b>Dat je
                    redelijk intrinsiek gemotiveerd bent betekent dat je het best leuk vindt om te studeren. Maar h&#233;&#233;l leuk vind
                    je het nou ook weer niet.</b> Dat je <b>in redelijke mate extrinsiek gemotiveerd bent wil zeggen dat je er
                    ook rekening mee houdt wat je met je studie kunt bereiken: een diploma, geld, status of de erkenning van de
                    mensen waar je van houdt.</b> Op zich is er niks mis met jouw motivatie. <b>W&#233;l zou je gelukkiger zijn als
                    je studie wat beter zou passen bij je werkelijke interesses.</b><br>
                Mijn mening over de waarheid van deze uitkomst:<br>
                De uitslag van deze test vind ik zeer goed hij is ongeveer zoals ik verwacht had, De eerste dikgedrukte zin laat zien dat ik niet super ben
                gemotiveerd door al het materiaal binnen deze studie, dat klopt HTML vind ik veel minder leuk dan bijvoorbeeld
                JS.(Voorbeeld is dat ik het leuker vind om een Steen-papier-schaar game te maken dan een webpagina)<br>
                Het 2e gehighlighte deel wijst op mijn extrensieke motivatie, hier zit ook veel waarheid achter. Ik studeer ook
                ICT omdat ik weet dat er veel vraag naar is en omdat ik weet dat het goed betaald word.<br>
                Ook zegt dit deel dat ik dit doe voor "status" waar zeker een kern van waarheid aan vast zit, want in de
                feedback van mijn sollicitatieopdracht(<a href="#1">1</a>) komt ook terug dat ik "sociaal begaan" ben. Dit is in meeste
                gevallen een goed iets omdat ik vaak aan de behoeften van anderen denk, maar ik ben op die manier ook vatbaar
                voor hun mening over mij. Op die manier ben ik ook vatbaar over hoe mensen denken dat ik geld verdien en hoeveel. </p>
